function somaQuadrados(){
    let valor1 = parseInt(prompt("Digite o 1º valor:"));
    let valor2 = parseInt(prompt("Digite o 2º valor:"));
    let valor3 = parseInt(prompt("Digite o 3º valor:"));
    let resultado
    resultado=(valor1*valor1)+(valor2*valor2)+(valor3*valor3)

    alert("O valor da soma do quadrado desses 3 valores é: "+resultado)
}